
-- =====================
-- ENUMS
-- =====================
CREATE TYPE public.user_role AS ENUM ('client', 'technician', 'admin');
CREATE TYPE public.machine_type AS ENUM ('color', 'noir_blanc');
CREATE TYPE public.contract_status AS ENUM ('actif', 'suspendu', 'terminé');
CREATE TYPE public.incident_category AS ENUM ('panne', 'maintenance', 'consommable', 'autre');
CREATE TYPE public.incident_priority AS ENUM ('basse', 'normale', 'haute', 'urgente');
CREATE TYPE public.incident_status AS ENUM ('nouveau', 'assigné', 'en_cours', 'résolu', 'fermé');
CREATE TYPE public.alert_type AS ENUM ('panne', 'toner_bas', 'autre');

-- =====================
-- PROFILES
-- =====================
CREATE TABLE public.profiles (
    id          UUID PRIMARY KEY REFERENCES auth.users(id) ON DELETE CASCADE,
    role        public.user_role NOT NULL DEFAULT 'client',
    full_name   TEXT,
    phone       TEXT,
    created_at  TIMESTAMPTZ NOT NULL DEFAULT now()
);

-- =====================
-- CLIENTS
-- =====================
CREATE TABLE public.clients (
    id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nom_client  TEXT NOT NULL UNIQUE,
    ninea       TEXT UNIQUE,
    email       TEXT,
    telephone   TEXT,
    adresse     TEXT,
    ville       TEXT,
    active      BOOLEAN NOT NULL DEFAULT true
);

-- =====================
-- MACHINES
-- =====================
CREATE TABLE public.machines (
    numero_serie    TEXT PRIMARY KEY,
    marque          TEXT NOT NULL,
    modele          TEXT NOT NULL,
    type            public.machine_type,
    localisation    TEXT,
    active          BOOLEAN NOT NULL DEFAULT true
);

-- =====================
-- CONTRACTS
-- =====================
CREATE TABLE public.contracts (
    id                  UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    numero_contrat      TEXT NOT NULL UNIQUE,
    client_id           BIGINT NOT NULL REFERENCES public.clients(id),
    machine_id          TEXT NOT NULL REFERENCES public.machines(numero_serie),
    date_debut          DATE NOT NULL,
    date_renouvellement DATE,
    lieu_installation   TEXT,
    statut              public.contract_status NOT NULL DEFAULT 'actif',
    created_at          TIMESTAMPTZ NOT NULL DEFAULT now()
);

-- =====================
-- CLIENT_PROFILES
-- Vincula usuario del portal con empresa cliente
-- =====================
CREATE TABLE public.client_profiles (
    profile_id  UUID NOT NULL REFERENCES public.profiles(id) ON DELETE CASCADE,
    client_id   BIGINT NOT NULL REFERENCES public.clients(id),
    verified_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    PRIMARY KEY (profile_id, client_id)
);

-- =====================
-- INCIDENTS
-- =====================
CREATE TABLE public.incidents (
    id                   UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    contract_id          UUID NOT NULL REFERENCES public.contracts(id),
    machine_id           TEXT NOT NULL REFERENCES public.machines(numero_serie),
    opened_by            UUID REFERENCES public.profiles(id),
    assigned_to          UUID REFERENCES public.profiles(id),
    title                TEXT NOT NULL,
    description          TEXT,
    category             public.incident_category NOT NULL DEFAULT 'panne',
    priority             public.incident_priority NOT NULL DEFAULT 'normale',
    status               public.incident_status NOT NULL DEFAULT 'nouveau',
    rapport_intervention TEXT,
    autres_pieces        TEXT,
    created_at           TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at           TIMESTAMPTZ NOT NULL DEFAULT now(),
    resolved_at          TIMESTAMPTZ,
    closed_at            TIMESTAMPTZ
);

