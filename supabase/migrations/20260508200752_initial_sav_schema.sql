-- =====================
-- INCIDENT_PARTS
-- =====================
CREATE TABLE public.incident_parts (
    incident_id UUID NOT NULL REFERENCES public.incidents(id) ON DELETE CASCADE,
    part_id     SMALLINT NOT NULL REFERENCES public.parts(id),
    PRIMARY KEY (incident_id, part_id)
);

-- =====================
-- INCIDENT_PHOTOS
-- =====================
CREATE TABLE public.incident_photos (
    id           UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    incident_id  UUID NOT NULL REFERENCES public.incidents(id) ON DELETE CASCADE,
    uploaded_by  UUID REFERENCES public.profiles(id),
    storage_path TEXT NOT NULL,
    created_at   TIMESTAMPTZ NOT NULL DEFAULT now()
);

-- =====================
-- INCIDENT_HISTORY (audit trail)
-- =====================
CREATE TABLE public.incident_history (
    id          UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    incident_id UUID NOT NULL REFERENCES public.incidents(id) ON DELETE CASCADE,
    changed_by  UUID REFERENCES public.profiles(id),
    old_status  TEXT,
    new_status  TEXT,
    comment     TEXT,
    created_at  TIMESTAMPTZ NOT NULL DEFAULT now()
);

-- =====================
-- CSAT_RESPONSES
-- =====================
CREATE TABLE public.csat_responses (
    id           UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    incident_id  UUID NOT NULL REFERENCES public.incidents(id),
    token        TEXT NOT NULL UNIQUE DEFAULT gen_random_uuid()::text,
    rating       SMALLINT CHECK (rating >= 1 AND rating <= 5),
    comment      TEXT,
    responded_at TIMESTAMPTZ
);

-- =====================
-- PRINCITY_ALERTS
-- =====================
CREATE TABLE public.princity_alerts (
    id               UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    received_at      TIMESTAMPTZ NOT NULL DEFAULT now(),
    client_raw       TEXT,
    client_id        BIGINT REFERENCES public.clients(id),
    machine_id       TEXT REFERENCES public.machines(numero_serie),
    site             TEXT,
    severity         TEXT,
    competence_level TEXT,
    ip_address       TEXT,
    mac_address      TEXT,
    modele           TEXT,
    description      TEXT,
    alert_type       public.alert_type DEFAULT 'autre',
    incident_id      UUID REFERENCES public.incidents(id),
    processed        BOOLEAN NOT NULL DEFAULT false,
    processed_at     TIMESTAMPTZ
);

-- =====================
-- TRIGGER: updated_at automático en incidents
-- =====================
CREATE OR REPLACE FUNCTION public.update_updated_at()
RETURNS TRIGGER
LANGUAGE plpgsql
SECURITY INVOKER
SET search_path = ''
AS $$
BEGIN
    NEW.updated_at = now();
    RETURN NEW;
END;
$$;

CREATE TRIGGER incidents_updated_at
    BEFORE UPDATE ON public.incidents
    FOR EACH ROW EXECUTE FUNCTION public.update_updated_at();

-- =====================
-- TRIGGER: crear profile automáticamente al registrar usuario
-- =====================
CREATE OR REPLACE FUNCTION public.handle_new_user()
RETURNS TRIGGER
LANGUAGE plpgsql
SECURITY INVOKER
SET search_path = ''
AS $$
BEGIN
    INSERT INTO public.profiles (id, full_name)
    VALUES (NEW.id, NEW.raw_user_meta_data->>'full_name');
    RETURN NEW;
END;
$$;

CREATE TRIGGER on_auth_user_created
    AFTER INSERT ON auth.users
    FOR EACH ROW EXECUTE FUNCTION public.handle_new_user();

